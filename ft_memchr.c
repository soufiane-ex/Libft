#include "libft.h"

void	*ft_memchr(const void *s, int c, size_t n)
{
	int		i ;
	char	*p ;

	i = 0 ;
	p = (char *)s ;
	while (n--)
	{
		if (p[i] == c)
			return (&p[i]);
		i++ ;
	}
	return (NULL);
}
